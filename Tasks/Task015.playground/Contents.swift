// MARK: - Task conditions
//
// 1. Two sets `set1` and `set2' are given.
// 2. Create a new set `union` that includes all the numbers from both.

// MARK: - Solution

let set1 = Set([11, 78, 66, 36, 72])
let set2 = Set([57, 83, 29, 42, 95])
// Beginning of the solution
let resultSet = set1.union(set2)
print(resultSet)
// End of the solution
