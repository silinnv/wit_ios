// MARK: - Task conditions
//
// 1. Two sets `set1` and `set2' are given.
// 2. Find out if `set2` is a subset of `set1`.
// 3. Put the result in the variable `isSubset`

// MARK: - Solution

let set1 = Set([11, 78, 66, 36, 83, 57, 29, 42])
let set2 = Set([57, 83, 29, 42, 66])
// The beginning of the solution
let result = set2.isSubset(of: set1)
print(result)
// Completion of the solution

